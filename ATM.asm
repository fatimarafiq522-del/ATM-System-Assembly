WITHDRAW MACRO TOTAL_CASH,CASH
CALL CLEAR_SCREEN
CALL NEW_LINE
MESSAGE B2
CALL NEW_LINE
MESSAGE S16

MOV CASH,0
MOV BL,10 

INPUT:
MOV AH,1
INT 21H
CMP AL,13
JE TERMINATE

SUB AL,30H
MOV AH,0
MOV CX,AX
MOV AX,CASH
MUL BL
ADD AX,CX
MOV CASH,AX

JMP INPUT

TERMINATE:
MOV BX,TOTAL_CASH
SUB BX,CASH
MOV TOTAL_CASH,BX 
ENDM


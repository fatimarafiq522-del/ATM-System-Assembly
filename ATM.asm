MAIN PROC
MOV AX,@DATA
MOV DS,AX

START:
CALL CLEAR_SCREEN

MESSAGE S1
CALL NEW_LINE
MESSAGE S2
CALL NEW_LINE
MESSAGE S3
CALL NEW_LINE
MESSAGE S15

CALL SELECT_CHOICE

CMP CHOICE,1
JE LOGIN_F

CMP CHOICE,2
JE CREATE_NEW_F

CMP CHOICE,3 
JE EXIT

MESSAGE S9
JMP START

LOGIN_F: 
CALL LOGIN

CREATE_NEW_F: 
CALL CREATE_NEW

EXIT:
MOV AH,4CH

NEW_LINE PROC
MOV DL,10
MOV AH,2
INT 21H

MOV DL,13
MOV AH,2
INT 21H
RET
NEW_LINE ENDP

SELECT_CHOICE PROC
CALL NEW_LINE
MESSAGE S4
MOV AH,1
INT 21H
SUB AL,48
MOV CHOICE,AL
RET
SELECT_CHOICE ENDP

